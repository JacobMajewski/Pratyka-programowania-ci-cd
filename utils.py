"""Basic math module"""


def add(a: int, b: int) -> int:
    """This function returns sum of two inputs"""
    return a + b


def subtract(a: int, b: int) -> int:
    """This function returns diffrence of two inputs"""

    return a - b


def multiply(a: int, b: int) -> int:
    """This function returns product of two inputs"""

    return a * b


def divide(a: int, b: int) -> float:
    """This function returns float result division of two inputs"""

    return a / b


